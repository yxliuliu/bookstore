DROP TABLE AUTHOR;
DROP TABLE BOOK;

CREATE TABLE AUTHOR (
     ID NUMBER(10) GENERATED BY DEFAULT ON NULL AS IDENTITY (START WITH 1 INCREMENT BY 1 MAXVALUE 2147483647 CYCLE NOCACHE) NOT NULL
    ,NAME VARCHAR2(50) NOT NULL
    ,BIRTHDAY DATE NOT NULL
    ,CREATED_DATE TIMESTAMP(6) NOT NULL
);

CREATE TABLE BOOK (
     ISBN VARCHAR2(50) NOT NULL CONSTRAINT BOOK_PK PRIMARY KEY
    ,TITLE VARCHAR2(50) NOT NULL
    ,AUTHORS VARCHAR2(50) NOT NULL
    ,YEAR NUMBER(4) NOT NULL
    ,PRICE NUMBER(10,2) NOT NULL
    ,GENRE VARCHAR2(50) NOT NULL
    ,CREATED_DATE TIMESTAMP(6) NOT NULL
    ,MODIFIED_DATE TIMESTAMP(6) NOT NULL
);

CREATE INDEX AUTHOR_IDX1 ON AUTHOR(NAME, BIRTHDAY);

CREATE INDEX BOOK_IDX1 ON BOOK(TITLE);
